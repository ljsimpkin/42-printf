#include "libft.h"

int	ft_number_len(int nb)
{
	int i;

	i = 1;
	if (nb == -2147483648)
	{
		i += 2;
		nb = 147483648;
	}
	if (nb < 0)
	{
		nb = nb * -1;
		i++;
	}
	while (nb > 9)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
